import UIKit
class AppFonts {
    static func light (size : CGFloat) -> UIFont {
        return  UIFont(name: "JFFlat-light", size: size)!
    }
    static func medium (size : CGFloat) -> UIFont {
        return  UIFont(name: "JFFlat-medium", size: size)!
    }
    static func regular (size : CGFloat) -> UIFont {
        return  UIFont(name: "JFFlat-regular", size: size)!
    }
    
}
